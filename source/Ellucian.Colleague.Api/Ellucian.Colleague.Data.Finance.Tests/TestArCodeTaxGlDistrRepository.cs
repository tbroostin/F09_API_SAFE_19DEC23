                if (code == "107")
                {
                    record.ArctdArInvoiceItem = "6";
                }
                if (code == "108")
                {
                    record.ArctdArInvoiceItem = "13235";
                }
                _arCodeTaxGlDistrs.Add(record);
            }
        }


        /// <summary>
        /// Gets all FACULTY records
        /// </summary>
        /// <returns>All FACULTY records</returns>
        public static IEnumerable<Faculty> GetAllFacultyRecords()
        {
            var facultyList = new List<Faculty>();
            var items = _allFaculty.Length / 8;
            for (var x = 0; x < items; x++)
            {
                try
                {
                    var depts = _allFaculty[x, 6].Split(',').ToList();
                    var deptPcts = new List<int?>();
                    var pct = depts.Any(d => !string.IsNullOrEmpty(d)) ? (int?)Math.Floor(100m / depts.Count()) : null;
                    foreach(var dept in depts)
                    {
                        deptPcts.Add(pct);
                    }
                    facultyList.Add(new Faculty()
                    {
                        Recordkey = _allFaculty[x, 0],
                        FacSpecialStatus = _allFaculty[x, 1],
                        FacHomeLocation = _allFaculty[x, 2],
                        FacAdviseFlag = _allFaculty[x, 3],
                        FacContractType = _allFaculty[x, 4],
                        FacAdvisees = _allFaculty[x, 5].Split(',').ToList(),
                        FacDepts = depts,
                        FacDeptPcts = deptPcts,
                        RecordGuid = Guid.NewGuid().ToString(),
                        RecordModelName = string.Empty
                    });
                }
                catch (Exception) { }
            }
            return facultyList.AsEnumerable();
        }

